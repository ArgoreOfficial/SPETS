#pragma once

///////////////////////////////////////////////////////////////////////////////////////

namespace wv
{

///////////////////////////////////////////////////////////////////////////////////////

	template <class T>
	class iSingleton
	{

	public:

		iSingleton<T>( const iSingleton<T>& ) = delete;
		
///////////////////////////////////////////////////////////////////////////////////////

		static T& getInstance( void )
		{
			static T instance;
			return instance;
		}

///////////////////////////////////////////////////////////////////////////////////////

	protected:
		iSingleton<T>( void ) { }

	};

///////////////////////////////////////////////////////////////////////////////////////

}